""" El comando type() permite ver
El tipo de dato de un objeto """

print("Tipos integer: ")
print(type(10))
print(type(0))
print(type(-5))
print(type(951951951951951951975775351))

print("Tipos float: ")
print(type(0.0))
print(type(10.0))
print(type(-3.14151617))
print(type(1e3))

print("Tipo Complex")
print(type(1+2j))

print("Tipo string")
print(type("1+2j"))
print(type('hola mundo'))
print(type(""" 
           Cadena 
           multiple
           linea 
           """))

print("Tipo boolean")
print(type(True))
print(type(False))
print(1<2)
print(1>2)

print("Tipo None")
print(type(None))